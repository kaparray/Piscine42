int		ft_iterative_factorial(int nb)
{
	int r;

	r = 1;
	if (nb < 0 || nb > 12)
	{
		r = 0;
	}
	else if (nb > 1)
	{
		while (nb > 1)
			r *= nb--;
	}
	else if (nb == 1 || nb == 0)
	{
		r = 1;
	}
	return (r);
}
