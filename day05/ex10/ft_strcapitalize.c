char	*ft_strcapitalize(char *str)
{
	int i;
	int k;

	i = 0;
	k = 1;
	while (str[i] != '\0')
	{
		str[i] > 64 && str[i] < 91 && k == 0 && (str[i] += 32);
		str[i] > 47 && str[i] < 58 && k == 1 ? (k = 0) : k;
		str[i] > 64 && str[i] < 91 && k == 1 && (k = 0);
		str[i] > 96 && str[i] < 123 && k == 1 && (str[i] -= 32) && (k = 0);
		(str[i] < 48 || (str[i] > 57 && str[i] < 65)) && (k = 1);
		(str[i] > 122 || (str[i] > 90 && str[i] < 97)) && (k = 1);
		i++;
	}
	return (str);
}
