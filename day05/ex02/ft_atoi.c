int		ft_atoi(char *str)
{
	int n;
	int i;
	int isneg;

	n = 0;
	i = 0;
	isneg = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\v') ||
		(str[i] == '\n') || (str[i] == '\r') || (str[i] == '\f'))
		i++;
	if (str[i] == '-')
		isneg = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		n = 10 * n + (str[i] - '0');
		i++;
	}
	if (isneg == 1)
		return (-n);
	return (n);
}
