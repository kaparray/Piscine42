char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = ((str[i] - 97) + 42) % 26 + 'a';
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = ((str[i] - 65) + 42) % 26 + 'A';
		i++;
	}
	return (str);
}
